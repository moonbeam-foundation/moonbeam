export const contractAddress = (makerAddress: string, nonce: number) => {
  if (!web3) {
    throw new Error("Must run web3.init() first");
  }
  return (
    "0x" +
    web3.utils
      .sha3(RLP.encode([makerAddress, nonce]) as any)
      .slice(12)
      .substring(14)
  );
};

export const importAccount = (privateKey: string) => {
  if (!web3) {
    throw new Error("Must run web3.init() first");
  }
  const account = web3.eth.accounts.privateKeyToAccount(privateKey);
  const mapStorageSlot = STORAGE_SLOT.padStart(64, "0");
  const mapKey = account.address.toString().substring(2).padStart(64, "0");
  const storageKey = web3Utils.sha3("0x".concat(mapKey.concat(mapStorageSlot)));
  return { ...account, storageKey };
};

export function compileSolidity(contractContent: string, contractName: string = "Test") {
  let result = JSON.parse(
    solc.compile(
      JSON.stringify({
        language: "Solidity",
        sources: {
          "main.sol": {
            content: contractContent,
          },
        },
        settings: {
          outputSelection: {
            "*": {
              "*": ["*"],
            },
          },
        },
      })
    )
  );

  const contract = result.contracts["main.sol"][contractName];
  return {
    bytecode: "0x" + contract.evm.bytecode.object,
    contract,
  };
}

// make a web3 request, adapted to manual seal testing
export async function customRequest(method: string, params: any[]) {
  return new Promise<JsonRpcResponse>((resolve, reject) => {
    (web3.currentProvider as any).send(
      {
        jsonrpc: "2.0",
        id: 1,
        method,
        params,
      },
      (error: Error | null, result?: JsonRpcResponse) => {
        if (error) {
          reject(
            `Failed to send custom request (${method} (${params.join(",")})): ${
              error.message || error.toString()
            }`
          );
        }
        resolve(result);
      }
    );
  });
}

